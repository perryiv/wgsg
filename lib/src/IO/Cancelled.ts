/**
 * Class representing a cancelled reader.
 * @class
 */
///////////////////////////////////////////////////////////////////////////////

export class Cancelled extends Error
{
	/**
	 * Construct the class.
	 * @param {string} [message] - An optional message for the error.
	 */
	public constructor ( message?: string )
	{
		super ( message );
		this.name = "Cancelled";
	}
}
