import type { IVector4 } from "../Types";


//////////////////////////////////////////////////////////////////////////
/**
 * Get the pre-multiplied color.
 * @param {IVector4} clearColor - The color to pre-multiply.
 * @returns {IVector4} The pre-multiplied color.
 */
//////////////////////////////////////////////////////////////////////////

export const preMultiplyColor = ( clearColor: Readonly<IVector4> ): IVector4 =>
{
	const c = clearColor;
	const a = c[3];
	return [
		c[0] * a, // Red
		c[1] * a, // Green
		c[2] * a, // Blue
		a         // Alpha
	];
}
