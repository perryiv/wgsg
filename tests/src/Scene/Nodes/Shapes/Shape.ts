export function test ()
{
	describe ( "Shape Class", function ()
	{
		it ( "Should be able to make a shape", function ()
		{
			const shape = new Shape();
			expect ( shape ).to.exist;
			expect ( shape instanceof Shape ).to.be.true;
		} );
	} );
};
